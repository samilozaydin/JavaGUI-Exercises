    private boolean Register() {
        
        boolean current = false;
        String mail;
        String name;
        String phone;
        String password;
        boolean isSmoker;
        char gender;
        
        if (txt_1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "lutfen mail alanini doldur");
            return current;
        }
        if (txt_2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "lutfen isim alanini doldur");
            return current;
        }
        if (txtf_phone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "lutfen telefonunu alanini doldur");
            return current;
            
        }
        if (txtp_1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "lutfen parola alanini doldur");
            return current;
            
        }
        if (!(rbtn_female.isSelected() || rbtn_male.isSelected())) {
            JOptionPane.showMessageDialog(this, "lutfen cinsiyet alanini doldur");
            return current;
            
        }
        
        if (!txtf_phone.isValid()) {
            JOptionPane.showMessageDialog(this, "Telefon numarasini dogru giriniz");
            return current;
            
        }
        
        mail = txt_1.getText();
        name = txt_2.getText();
        phone = txtf_phone.getText();
        password = txtp_1.getPassword().toString();
        if (rbtn_female.isSelected()) {
            gender = 'F';
        } else {
            gender = 'M';
        }
        isSmoker = rbtn_smoke.isSelected();
        
        String password3 = txtp_1.getText();
        String password2 = JOptionPane.showInputDialog(rootPane, "Sifreyi tekrar giriniz", "sifre yeniden girmewk", JOptionPane.INFORMATION_MESSAGE);
        if (password2 ==null ||!password2.equals(password3)) {
            JOptionPane.showMessageDialog(rootPane, "Sifreyi yanlÄ±s girdiniz");
            return current;
            
        }
        int value = JOptionPane.showConfirmDialog(this, "Gercekten kaydolmak istiyor musun?", "kontrol", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (value == JOptionPane.NO_OPTION) {
            return current;
            
        }
        
        Person person = new Person();
        current = true;
        person.gender = gender;
        person.name = name;
        person.isSmoker = isSmoker;
        person.mail = mail;
        person.password = password;
        person.phone = phone;
        
        this.personList.add(person);
        return current;
    }

    private void btn_applyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_applyActionPerformed
        
        Register();
    }//GEN-LAST:event_btn_applyActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        
        int value = JOptionPane.showConfirmDialog(rootPane, "Cikmadan once kaydetmek ister misiniz?", "Dikkat", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
        if (value == JOptionPane.YES_OPTION) {
            
            if (Register()) {
                this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            } else {
                this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
            }
        } else if (value == JOptionPane.NO_OPTION) {
            this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        } else {
            this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        }
    }//GEN-LAST:event_formWindowClosing

